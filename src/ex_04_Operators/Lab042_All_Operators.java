package ex_04_Operators;

public class Lab042_All_Operators {

	public static void main(String[] args) {
		
		
		int a = 10;
		int b =20;
		System.out.println(a+b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
		// Compound operator
		// += , -= , /= , *=
		int age =10;
		age +=10; // age =age+10
		System.out.println(age);
		age/= 10; //age = age/10
		System.out.println(age);
		
		// Bitwise operator
		// ~ Bitwise not
		// >> Right shift
		// << Left shift
		// ^ XOR
		
	}
}
