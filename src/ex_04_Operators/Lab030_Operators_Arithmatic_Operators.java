package ex_04_Operators;

public class Lab030_Operators_Arithmatic_Operators {

	public static void main(String[] args) {
		// Arithmatic Operators
		// + -> Addition
		// - -> Substraction
		// * -> Multiplcation
		// / -> Division
		// % -> Modulus 
		
		int a = 10;
		int b = 20;
		float c = 1.0f;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println(a/c); // output will be in float.
	}
}
